def count_wobble(strand1: str, strand2: str) -> bool:
    pairs = list(zip(strand1, strand2))
    pair_str = ''.join(['GU' if (a=='G' and b=='U') else
                        'UG' if (a=='U' and b=='G') else
                        'IU' if (a=='I' and b=='U') else
                        'UI' if (a=='U' and b=='I') else
                        '--'
                        for a, b in pairs])
    return pair_str.count('I')+pair_str.count('G')+pair_str.count('U')>=6

def unmod_count_wobble(strand1: str, strand2: str) -> bool:
    pairs = list(zip(strand1, strand2))
    pair_str = ''.join(['GU' if (a=='G' and b=='U') else
                        'UG' if (a=='U' and b=='G') else
                        '--'
                        for a, b in pairs])
    return pair_str.count('G')+pair_str.count('U')==4

def classify_duplexes(all_strands):
    guiu            = []
    guic            = []
    tripple_wobble  = []
    unmod_gu        = []
    unmod_double_gu = []

    for strand in all_strands:
        s1, s2 = strand.split('-')
        seq1 = sequences[s1]
        seq2 = sequences[s2][::-1]

        stacks = []
        for i in range(0, len(seq1)-1):
            curr_stack = seq1[i:i+2]+seq2[i:i+2]
            stacks.append(curr_stack)

        if count_wobble(seq1, seq2):
            tripple_wobble.append(strand)
        elif 'I' in seq1 or 'I' in seq2:
            for stk in stacks:
                if (stk in mod_ic_gu or stk[::-1] in mod_ic_gu) and strand not in guic:
                    guic.append(strand)
                elif (stk in mod_iu_gu or stk[::-1] in mod_iu_gu) and strand not in guiu:
                    guiu.append(strand)
        elif unmod_count_wobble(seq1, seq2):
            unmod_double_gu.append(strand)
        else:
            if ('G', 'U') in zip(seq1, seq2) or ('U', 'G') in zip(seq1, seq2):
                unmod_gu.append(strand)

    return guiu, guic, tripple_wobble, unmod_gu, unmod_double_gu

def get_seq(strand):
    s1, s2 = strand.split('-')
    seq1 = sequences[s1]
    seq2 = sequences[s2]
    return seq1, seq2


